                  <br>
									<p>There are a number of institutes that offer PGDCA, but the eligibility criteria of PGDCA is not uniform across these institutes. Take for example, IK Gujral Punjab Technical University, which doesn’t have any cutoff marks in the qualifying exam for admission, whereas for admission to the same course at Dibrugarh University, the applicant needs to score minimum 40% marks in the graduate exam (relaxation of 5% in minimum marks applies to reserved category candidates.</p>
									<p>Then in some cases, institutes specify a strict age limit for admission as part of PGDCA eligibility criteria. Vallabh Government College, Mandi is a case in point as it mandates that applicants be not more than 26 years of age ( 28 years for girls). So, before applying to a PGDCA programme, it is important that the applicant confirms the required PGDCA eligibility criteria.</p>
									<ol>
										<li>Qualifying exam: The candidate needs to be a graduate in any discipline (BA, B.Com, B.Sc, BBA, BCA, B.Tech etc.).</li>
										<li>Minimum marks: There is generally no minimum marks, barring a few exceptions like Dibrugarh University.</li>
										<li>Subject combination: While a simple graduation is generally enough, institutes like IGNOU and Indian Statistical Institute, Kolkata demand that the candidate also studied Mathematics as one of the subjects in their graduate/post-graduate degree.</li>
										<li>Age limit: Again most institutes don’t have any upper or lower age limit. Exceptions include Vallabh Government College, Mandi.</li>
										
